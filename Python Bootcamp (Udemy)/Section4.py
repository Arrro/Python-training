# Comparison operators
2 == 2 # True
2 == 1 # False
'hello' == 'bye' #False, caps count in comparison. Can also compare types
3 != 2 # True; Inequality; Above is equality

# Comparison operators with logicical operators
# and, or, not
1 < 2 and 2 < 3 
'h' == 'h' or 2 == 2 # True
not(1 == 1) # False
